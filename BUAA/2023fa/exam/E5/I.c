#include<stdio.h>

unsigned short ma[256][256];
unsigned short i, j;

void f(unsigned short x) {
    if(x == 1) { 
        ma[0][0] |= (1 << 5);
        return;
    }
    for(i = (x>>1)+1; i <= x; ++i) {
        for(j = (x>>1)+1; j <= x; ++j) {
            ma[i>>2][j>>2] |= (1 << (((j%4)<<2)+(i%4)));
        }
    }
    f(x>>1);
    for(i = 0; i <= (x>>1); ++i) {
        for(j = 0; j <= (x>>1); ++j) {
            if((ma[i>>2][j>>2] >> (((j%4)<<2)+(i%4))) & 1) {
                ma[(x-i)>>2][j>>2] |= (1 << (((j%4)<<2)+((x-i)%4)));
                ma[i>>2][(x-j)>>2] |= (1 << ((((x-j)%4)<<2)+(i%4)));
            }
        }
    }
}

int main() {
    unsigned short k;
    scanf("%hu", &k);
    if(k == 0) return printf("11\n11"), 0;
    f((1<<k)-1);
    for(i = 0; i < (1 << (k+1)); ++i) {
        for(j = 0; j < (1 << (k+1)); ++j) {
            if(i <= (1 << k)-1) {
                if(j <= (1 << k)-1) {
                    printf("%c", ((ma[i>>2][j>>2] >> (((j%4)<<2)+(i%4))) & 1) ? ' ' : '1');
                }
                else {
                    printf("%c", ((ma[i>>2][((1<<(k+1))-1 - j)>>2] >> (((((1<<(k+1))-1 - j)%4)<<2)+(i%4))) & 1) ? ' ' : '1');
                }
            }
            else {
                if(j <= (1 << k)-1) {
                    printf("%c", ((ma[((1<<(k+1))-1 - i)>>2][j>>2] >> (((j%4)<<2)+(((1<<(k+1))-1 - i)%4))) & 1) ? ' ' : '1');
                }
                else {
                    printf("%c", ((ma[((1<<(k+1))-1 - i)>>2][((1<<(k+1))-1 - j)>>2] >> (((((1<<(k+1))-1 - j)%4)<<2)+(((1<<(k+1))-1 - i)%4))) & 1) ? ' ' : '1');
                }
            }
        }
        puts("");
    }
    return 0;
}
